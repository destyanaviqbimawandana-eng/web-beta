// ======================= FUNGSI GANTI BAHASA =======================
function changeLanguage(lang) {
    currentLang = lang;
    localStorage.setItem("selectedLanguage", currentLang);
    setActiveLanguage();
    
    // Refresh konten berita jika perlu
    if (typeof loadNews === 'function') {
        loadNews();
    }
}

// ======================= INISIALISASI =======================
$(document).ready(function() {
    setActiveLanguage();
    $("#langId").on("click", function() { changeLanguage("id"); });
    $("#langEn").on("click", function() { changeLanguage("en"); });
});